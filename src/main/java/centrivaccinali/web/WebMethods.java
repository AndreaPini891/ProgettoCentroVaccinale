package centrivaccinali.web;

/**
 * The enum Web methods.
 */
public enum WebMethods {
    /**
     * Get web methods.
     */
    GET,
    /**
     * Post web methods.
     */
    POST,
    /**
     * Put web methods.
     */
    PUT,
    /**
     * Patch web methods.
     */
    PATCH,
    /**
     * Delete web methods.
     */
    DELETE
}
